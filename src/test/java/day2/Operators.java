package day2;

public class Operators {
    public static void main(String[] args) {
        /**
         *   + - * / %
         *   + +
         *   - -
         */

        int x = 50;
        int y = 3;
        int sum = x+y;
        System.out.println("Addtion of two numbers: "+sum);

        int sub = x - y ;
        System.out.println("sub of two numbers: "+sub);

        int division = x/y ;
        System.out.println("div of two numbers: "+division);

        int rem = x%y ;
        System.out.println("reminder of two numbers: "+rem);

        int mul = x*y ;
        System.out.println("multiplication  of two numbers: "+mul);


    }
}
